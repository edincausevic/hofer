          <br/>
          Ihr, <img src="img/signature.png" alt="Image of Hofers Signature">
        </p>
      </div>
      <div class="info-box">
        <img src="img/hearth.png" class="img-icon" alt="tooth icon photo"/>
        <h3>Mikroskopische Endodontie</h3>
        <p>Durch den Einsatz des Operationsmikroskops bei der Wurzelbehandlung können heutzutage auch sehr stark entzündete und schwer behandelbare Zähne
          gerettet werden. Bei dieser minimalinvasiven, meist schmerzfreien Behandlung wird die erkrankte Zahnwurzel unter bis zu 25-facher Vergrößerung mit
          modernsten Instrumenten bearbeitet, desinfiziert und anschließend wurzelgefüllt. Ein starkes Fundament ist für den langfristigen Erhalt Ihrer
          natürlichen Zähne unabdingbar. Gesunde Zähne von der Wurzel bis zur Spitze!</p>
      </div>
      <div class="info-box">
        <img src="img/medicine.png" class="img-icon" alt="medicin icon photo"/>
        <h3>Wahlarztordination</h3>
        <p>
          Als Wahlärzte werden Privatärzte bezeichnet, die in keinem Vertragsverhältnis zu den öffentlichen Krankenkassen stehen - dadurch haben Sie den Status
          als Privatpatient. So können wir uns für Diagnose, Besprechung und Therapie ausführlich Zeit nehmen und weitreichende ärztliche Leistungen anbieten.
          Die Kosten Ihrer Behandlung werden direkt mit Ihnen als Patient verrechnet. Gerne sind wir Ihnen bei der Kostenrückerstattung mit Ihrer Krankenkasse
          behilflich und übernehmen den administrativen Aufwand dafür.
        </p>
